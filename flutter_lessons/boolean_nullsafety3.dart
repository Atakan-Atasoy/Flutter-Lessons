void main(List<String> args) {
  int age = 28;
  print(age);
  age = 32; // you can change the value of the variable.
  print(age); // change the value of the variable and see results !!!

  double number = 10;
  print(number); // it will print "10.0" because the data type is double.

  int weight = 180;
  print(weight); // it will print "180" because the data type is int.

  //int numberunknown; // it will print "null" because the data type is int and it is not assigned any value.
  // print(numberunknown); you can not print unassigned variable.

  bool isTrue = true;
  print(isTrue); // it will print "true" because the data type is bool.

  bool isFalse = false;
  print(isFalse); // it will print "false" because the data type is bool.

  String name = "John";
  print(name); // it will print "John" because the data type is String.

  double? number2 = null;
  print(number2);

  int hexadecimal = 0xEADEBAEE; // hexadecimal value
  print(hexadecimal);
}
